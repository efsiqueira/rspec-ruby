class Calculator

    def sum(first_number, second_number) 
      result = first_number + second_number
    end

    def sub(first_number, second_number)
      result = first_number - second_number
    end

    def mul(first_number, second_number)
      result = first_number * second_number
    end

    def div(first_number, second_number)
      result = first_number / second_number
    end
end